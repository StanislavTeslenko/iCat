import  UIKit

extension UIColor {
    
    static func mainBGColor() -> UIColor {
        return UIColor(named: "MainBackgroundColor")!
    }
    
    static func mainTextColor() -> UIColor {
        return UIColor(named: "MainTextColor")!
    }

}
